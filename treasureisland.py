print("Welcome to Treasure island.\nYour mission is to find the treasure.")

direc = input(
  "You're at a cross road. Where do you want to go ? Type 'left' or 'right'\n")
direc.lower()

if direc == "left":

  action = input(
    "You came to a lake. There is an island in the middle of the lake. Type 'wait' to wait for a boat. Type 'swim' to swim across.\n"
  )
  action.lower()

  if action == "wait":

    door = input(
      "You have arrived at the island unharmed. There is a house with 3 doors. One red, one yellow and one blue. Which color do you choose?\n"
    )
    door.lower()

    if door == "red":
      print("You were burned by fire. GAME OVER !!")
    elif door == "blue":
      print("You were eaten by Beasts. GAME OVER !!")
    elif door == "yellow":
      print("You WON !!")
    else:
      print("You chose the door that doesn't exist. GAME OVER !!")

  else:
    print("You were attacked by trout. GAME OVER !!")

else:
  print("You fell into the hole. GAME OVER !!")
